
/* début de la classe */ class boucle {

    /* début de la fonction main() */
    public static void main(String[] args) {

        /* Déclaration des variables */
        int i;

        /* initialisation des variables */
        i=1;

        /* début de la boucle while : tant que i est inférieur */     /* ou égal à 20 on l’affiche et on l’incrémente : */
        while(i<=20) {
            System.out.println(i);         i=i+1;       } /* fin de la boucle while */

    } /* fin de la fonction main() */ } /* fin de la classe */



